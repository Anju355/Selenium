package datatypeDemo;

public class IntDemo {

	public static void main(String[] args) {
		
		byte marks = 98;
		System.out.println("My Marks are "+marks);
		
		//Defining short as byte dont contain value more than 127
		short total_money=150;
		System.out.println("Total money I have is "+total_money);
		
		//Defining int as short dont contain larger value
		int mymobile = 032145710;
		System.out.println("My mobile number is "+mymobile);
		
		//Defining long as long can contain big range
		long bigNum = 1457195820;
		System.out.println("Long number to show "+bigNum);
		

	}

}
