package conditionalStatements;

public class switchDemo {

	public static void main(String[] args) {
		// Switch Demo
		
		int x = 4;
		
		switch(x)
		{
		case 1:
			System.out.println("Performance is good");
			break;
			
		case 2:
			System.out.println("Slow performer");
			break;
		
		case 3:
			System.out.println("Meet expectations");
			break;
		
		case 4:
			System.out.println("Weldone keep rocking");
			break;
			
		case 5:
			System.out.println("Employe of the year");
			break;
			
		default:
			System.out.println("You are fired");
			
		}

	}

}
